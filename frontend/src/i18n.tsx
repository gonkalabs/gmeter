export type TranslationKey = keyof typeof en;
export type TFunction = (key: TranslationKey, vars?: Record<string, string | number>) => string;

const dictionaries = { en, ru };

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: TFunction;
  formatDate: (value: string | number | Date, options?: Intl.DateTimeFormatOptions) => string;
  formatNumber: (value: number) => string;
  plural: (count: number, one: string, few: string, many: string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function initialLocale(): Locale {
  const saved = localStorage.getItem("gmeter-locale");
  if (saved === "en" || saved === "ru") return saved;
  return navigator.language.toLowerCase().startsWith("ru") ? "ru" : "en";
}

function localeTag(locale: Locale) {
  return locale === "ru" ? "ru-RU" : "en-US";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(initialLocale);

  useEffect(() => {
    localStorage.setItem("gmeter-locale", locale);
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    const t: TFunction = (key, vars) => {
      let text = dictionaries[locale][key] ?? en[key] ?? key;
      for (const [name, value] of Object.entries(vars ?? {})) {
        text = text.replaceAll(`{${name}}`, String(value));
      }
      return text;
    };

    return {
      locale,
      setLocale,
      t,
      formatDate: (value, options) =>
        new Date(value).toLocaleString(localeTag(locale), options),
      formatNumber: (value) => value.toLocaleString(localeTag(locale)),
      plural: (count, one, few, many) => pluralForm(locale, count, one, few, many),
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return value;
}

export function translate(
  locale: Locale,
  key: TranslationKey,
  vars?: Record<string, string | number>
) {
  let text = dictionaries[locale][key] ?? en[key] ?? key;
  for (const [name, value] of Object.entries(vars ?? {})) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}

export function pluralForm(
  locale: Locale,
  count: number,
  one: string,
  few: string,
  many: string
) {
  if (locale !== "ru") return count === 1 ? one : many;
  const mod10 = count % 10;
  const mod100 = count % 100;
  if (mod10 === 1 && mod100 !== 11) return one;
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return few;
  return many;
}
